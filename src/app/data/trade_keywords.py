def get_keywords_for_trade(trade_category: str) -> list:
    """
    Get the list of keywords for a specific trade category.

    Args:
        trade_category: Name of the trade category

    Returns:
        List of keywords or empty list if not found
    """
    return TRADE_KEYWORDS.get(trade_category, [])


def get_all_trade_categories() -> list:
    """
    Get all available trade categories.

    Returns:
        List of trade category names
    """
    return list(TRADE_KEYWORDS.keys())
